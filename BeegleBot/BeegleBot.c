void LDrive(int prw)
{
	motor[BLDrive] = motor[FLDrive] = prw;
}
void RDrive(int prw)//function to turn on the right drive
{
	motor[BRDrive] = motor[FRDrive] = prw;
}
void lift(int pwr)//function for the lift
{
	motor[LFLift] = motor[LBLift] = motor[RFLift] = motor[RBLift] = pwr;
}

void claw(int pwr)
{
	motor[lClaw] = motor[rClaw] = pwr;
}

void goRobit(int pwrR, int pwrL, int time)
{
	LDrive(pwrL);
	RDrive(pwrR);
	wait1Msec(time);
	LDrive(0);
	RDrive(0);
}

void goLift(int pwr, int time)
{
	lift(pwr);
	wait1Msec(time);
	motor[LFLift] = motor[LBLift] = motor[RFLift] = motor[RBLift] = 0;
}

void goClaw(int pwr, int time)
{
	claw(pwr);
	wait1Msec(time);
	claw(0);
}

//Drive options
void arcade(int move, int rot)
{
	LDrive(move + rot);
	RDrive(move - rot);
}

void tank(int pwrL, int pwrR)
{
	LDrive(pwrL);
	RDrive(pwrR);
}

