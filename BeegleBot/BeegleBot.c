void LDrive(int prw){
	motor[BLWheel] = motor[FLWheel] = prw;
}
void RDrive(int prw){
	motor[BRWheel] = motor[FRWheel] = prw;
}
void lift(int pwr){
	motor[LFLift] = motor[LBLift] = motor[RFLift] = motor[RBLift] = pwr;
}

void claw(int pwr){
	motor[lClaw] = motor[rClaw] = pwr;
}

void goRobit(int pwrR, int pwrL, int time){
	LDrive(pwrL);
	RDrive(pwrR);
	wait1Msec(time);
	LDrive(0);
	RDrive(0);
}

void goLift(int pwr, int time){
	lift(pwr);
	wait1Msec(time);
	motor[LFLift] = motor[LBLift] = motor[RFLift] = motor[RBLift] = 0;
}

void goClaw(int pwr, int time){
	claw(pwr);
	wait1Msec(time);
	claw(0);
}

//Drive options
